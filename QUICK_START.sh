read -p "Pressione ENTER quando terminar..."

# PASSO 4: Vercel Front-End
echo ""
echo "================================"
echo "🎨 PASSO 4: Deploy Front-End"
echo "================================"
echo ""
echo "1. Volte a https://vercel.com/dashboard"
echo "2. New Project"
echo "3. Selecione MESMO repositório"
echo "4. Configuração:"
echo "   - Root Directory: client"
echo "   - Framework: Vite"
echo "   - Build: npm run build"
echo "   - Output: dist"
