echo "  git branch -M main"
echo "  git push -u origin main"
echo ""
read -p "Pressione ENTER quando terminar..."

# PASSO 3: Vercel Back-End
echo ""
echo "================================"
echo "🔙 PASSO 3: Deploy Back-End"
echo "================================"
echo ""
echo "1. Abra https://vercel.com/dashboard"
echo "2. New Project"
echo "3. Conecte seu repositório GitHub (papodelivro)"
echo "4. Configuração:"
echo "   - Root Directory: server"
echo "   - Framework: Other (Node.js)"
echo "   - Build: npm run build"
