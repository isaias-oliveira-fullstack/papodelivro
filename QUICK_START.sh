echo "1. Abra https://github.com/new"
echo "2. Crie um repositório chamado: papodelivro"
echo ""
echo "Depois, copie e execute:"
echo ""
echo "  git remote add origin https://github.com/SEU-USUARIO/papodelivro.git"
