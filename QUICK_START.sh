echo ""
echo "  Terminal 1 (Back-end):"
echo "    cd server"
echo "    npm install"
echo "    npm run dev"
echo ""
echo "  Terminal 2 (Front-end):"
echo "    cd client"
echo "    npm install"
echo "    npm run dev"
echo ""
echo "  ✓ Abra http://localhost:5173"
echo "  ✓ Teste se está funcionando"
echo ""
read -p "Pressione ENTER quando tudo estiver funcionando..."

# PASSO 2: GitHub
echo ""
echo "================================"
echo "🐙 PASSO 2: Criando repositório GitHub..."
